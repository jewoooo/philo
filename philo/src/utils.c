#include "../include/philosopher.h"

int	ft_atoi(char *s)
{
	int	res;
	int	flag;

	flag = 0;
	while (*s == '-')
	{
		flag = 1;
		s++;
	}
	while (*s == '+')
		s++;
	res = 0;
	while (*s >= '0' && *s <= '9')
	{
		res *= 10;
		res = res + (*s - '0');
		s++;
	}
	if (flag == 1)
		res *= (-1);
	return (res);
}
